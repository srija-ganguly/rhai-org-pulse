/**
 * Build markdown planning prep summary for Confluence / meetings.
 */
export function buildPlanningPrepMarkdown({ pmDisplayName, release, stats, items, fetchedAt }) {
  const lines = []
  const date = fetchedAt ? new Date(fetchedAt).toLocaleDateString() : new Date().toLocaleDateString()

  lines.push(`# Pipeline planning prep — ${pmDisplayName}`)
  lines.push('')
  lines.push(`**Release:** ${release} · **Generated:** ${date}`)
  lines.push('')
  lines.push('## Summary')
  lines.push('')
  lines.push(`- **Need action:** ${stats.needAction}`)
  lines.push(`- **In progress:** ${stats.inProgress}`)
  lines.push(`- **Ready for planning:** ${stats.ready}`)
  lines.push(`- **Complete:** ${stats.complete}`)
  lines.push(`- **Total items:** ${stats.total}`)
  lines.push('')

  const actionable = items.filter(i => i.pmActionable)
  if (actionable.length > 0) {
    lines.push('## Action items')
    lines.push('')
    for (const item of actionable) {
      lines.push(`### ${item.id} — ${item.stage.label}`)
      lines.push('')
      lines.push(`**Summary:** ${item.summary}`)
      lines.push('')
      lines.push(`**Jira:** ${item.links.jira}`)
      if (item.waitDays > 0) {
        lines.push(`**Waiting:** ${item.waitDays} days`)
      }
      if (item.playbook?.steps?.[0]) {
        lines.push(`**Next step:** ${item.playbook.steps[0]}`)
      }
      if (item.playbook?.links?.length) {
        lines.push('')
        lines.push('**Resources:**')
        for (const link of item.playbook.links) {
          lines.push(`- [${link.label}](${link.url})`)
        }
      }
      lines.push('')
    }
  } else {
    lines.push('## Action items')
    lines.push('')
    lines.push('_No PM action items at this time._')
    lines.push('')
  }

  const waiting = items.filter(i => !i.pmActionable && i.stage.id !== 'epics-complete' && i.stage.id !== 'complete')
  if (waiting.length > 0) {
    lines.push('## Waiting on others')
    lines.push('')
    for (const item of waiting) {
      lines.push(`- **${item.id}** — ${item.stage.label}: ${truncate(item.summary, 80)} ([Jira](${item.links.jira}))`)
    }
    lines.push('')
  }

  lines.push('---')
  lines.push('_Generated by PM Pipeline Cockpit_')

  return lines.join('\n')
}

function truncate(s, max) {
  if (!s || s.length <= max) return s || ''
  return s.slice(0, max - 1) + '…'
}

export async function copyToClipboard(text) {
  if (navigator.clipboard?.writeText) {
    await navigator.clipboard.writeText(text)
    return true
  }
  return false
}
